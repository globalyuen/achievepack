# Load each JSON file, merge new translation keys, and write back
for lang, filepath in locale_files.items():
    if not os.path.exists(filepath):
        print(f"Warning: File {filepath} does not exist.")
        continue

    try:
        with open(filepath, "r", encoding="utf-8") as f:
            data = json.load(f)
        
        # Ensure path exists: seoPages -> pages
        if "seoPages" not in data:
            data["seoPages"] = {}
        if "pages" not in data["seoPages"]:
            data["seoPages"]["pages"] = {}

        # Merge new pages translations
        data["seoPages"]["pages"]["homeVsIndustrialCompost"] = translations[lang]["homeVsIndustrialCompost"]
        data["seoPages"]["pages"]["biodegradableVsCompostable"] = translations[lang]["biodegradableVsCompostable"]

        # Save back to file with proper formatting (4 spaces indent)
        with open(filepath, "w", encoding="utf-8") as f:
            json.dump(data, f, ensure_ascii=False, indent=4)
        print(f"Successfully updated translations for language '{lang}' in {filepath}")
    except Exception as e:
        print(f"Error updating file {filepath}: {e}")
